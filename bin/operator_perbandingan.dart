void main() {
  //? Operator Perbandingan
  /**
   * Operator perbandingan yaitu digunakan untuk membandingkan suatu nilai yang hasilnya berniali boolean
   * operator perbandingan bisa digunakan pada tipe data number dan string
   * == (sama dengan)
   * != (tidak sama dengan)
   * > (lebih dari)
   * < (kecil dari)
   * >= (lebih dari atau sama dengan)
   * <= (kecil dari atau sama dengan)
   */

  /* Tipe data number */
  print(10 == 10);
  print(10 != 10);
  print(10 > 10);
  print(10 < 10);
  print(10 >= 10);
  print(10 <= 10);

  /* Tipe data string */
  print('eko' == 'eko');
  print('eko' != 'eko');
}
