void main() {
  //? Pertambahan
  var result1 = 10 + 10;
  print(result1);

  //? Pengurangan
  var result2 = 100 - 10;
  print(result2);

  //? Nilai negatif
  var result3 = -5;
  print(result3);

  //? Nilai Positif
  var result4 = 5;
  print(result4);

  //? Perkalian
  var result5 = 5 * 5;
  print(result5);

  //? Pembagian dengan hasil double
  var result6 = 10 / 3;
  print(result6);

  //? Pembagian dengan hasil int
  var result7 = 10 ~/ 3;
  print(result7);

  //? Sisa bagi (modulo)
  var result8 = 10 % 10;
  print(result8);
}
